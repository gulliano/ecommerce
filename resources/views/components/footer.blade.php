        // Fonction pour appeler l'API Gemini
        async function callGeminiAPI(prompt) {
            let chatHistory = [];
            chatHistory.push({ role: "user", parts: [{ text: prompt }] });
            const payload = { contents: chatHistory };
            const apiKey = ""; // Laissez la clé API vide, elle sera fournie par l'environnement Canvas
            const apiUrl = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent?key=${apiKey}`;

            try {
                const response = await fetch(apiUrl, {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify(payload)
                });
                const result = await response.json();

                if (result.candidates && result.candidates.length > 0 &&
                    result.candidates[0].content && result.candidates[0].content.parts &&
                    result.candidates[0].content.parts.length > 0) {
                    return result.candidates[0].content.parts[0].text;
                } else {
                    console.error("Structure de réponse inattendue de l'API Gemini:", result);
                    return "Désolé, je n'ai pas pu générer de réponse.";
                }
            } catch (error) {
                console.error("Erreur lors de l'appel à l'API Gemini:", error);
                return "Une erreur est survenue lors de la communication avec l'IA.";
            }
        }

        // --- Fonctionnalité "Conseil de Style" pour chaque produit ---
        document.addEventListener('DOMContentLoaded', () => {
            const styleTipButtons = document.querySelectorAll('.generate-style-tip-btn');

            styleTipButtons.forEach(button => {
                button.addEventListener('click', async () => {
                    const productCard = button.closest('.bg-white.rounded-xl.shadow-md');
                    const productName = productCard.querySelector('[data-product-name]').dataset.productName;
                    const styleTipOutput = productCard.querySelector('.style-tip-output');
                    const spinner = button.querySelector('.spinner');
                    const buttonTextSpan = button.querySelector('span');

                    // Afficher le spinner et désactiver le bouton
                    spinner.classList.remove('hidden');
                    buttonTextSpan.classList.add('hidden');
                    button.disabled = true;
                    styleTipOutput.classList.add('hidden'); // Cacher l'ancien conseil

                    const prompt = `Donne-moi un conseil de style pour porter un "${productName}" de manière moderne et élégante. Sois concis (environ 20-30 mots) et créatif.`;

                    try {
                        const tip = await callGeminiAPI(prompt);
                        styleTipOutput.textContent = tip;
                        styleTipOutput.classList.remove('hidden'); // Afficher le nouveau conseil
                    } catch (error) {
                        styleTipOutput.textContent = "Erreur: Impossible de générer un conseil de style.";
                        styleTipOutput.classList.remove('hidden');
                        console.error("Erreur lors de la génération du conseil de style:", error);
                    } finally {
                        // Cacher le spinner et réactiver le bouton
                        spinner.classList.add('hidden');
                        buttonTextSpan.classList.remove('hidden');
                        button.disabled = false;
                    }
                });
            });

